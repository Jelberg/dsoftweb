public class Sport {
    private int    _id;
    private String _name;
    private float  _met;

    public Sport() {

    }

    public int getId() {
        return _id;
    }

    public void setId(int _id) {
        this._id = _id;
    }

    public String getName() {
        return _name;
    }

    public void setName(String _name) {
        this._name = _name;
    }

    public float getMet() {
        return _met;
    }

    public void setMet(float _met) {
        this._met = _met;
    }

}
